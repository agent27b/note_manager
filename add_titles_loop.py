def main():
    # Создаем пустой список для хранения заголовков
    notes = []

    # Запускаем бесконечный цикл для ввода заголовков
    while True:
        title = input("Введите заголовок (оставьте пустым, или введите 'стоп' для завершения): ")

        # Если пользователь ввел пустую строку или команду "стоп"
        if title.strip() == "" or title.strip().lower() == "стоп":
            break  # Ввод завершён, выход из цикла

        # Если заголовок не пустой, то добавляем в список
        notes.append(title)

    # Выводим список заголовков
    print("\nЗаголовки заметки:")
    for note in notes:
        print(f"- {note}")

# Запускаем основную функцию
if __name__ == "__main__":
    main()